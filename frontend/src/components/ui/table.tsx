import * as React from "react";

import { cn } from "@/lib/utils";

function Table({ className, ...props }: React.ComponentProps<"table">) {
  return (
    <div className="relative w-full overflow-auto">
      <table
        data-slot="table"
        className={cn(
          "w-full caption-bottom border-2 border-border dark:border-white text-sm",
          className
        )}
        {...props}
      />
    </div>
  );
}

function TableHeader({ className, ...props }: React.ComponentProps<"thead">) {
  return (
    <thead
      data-slot="table-header"
      className={cn(
        "[&_tr]:border-b-2 [&_tr]:border-border dark:[&_tr]:border-white text-main-foreground dark:text-white",
        className
      )}
      {...props}
    />
  );
}

function TableBody({ className, ...props }: React.ComponentProps<"tbody">) {
  return (
    <tbody
      data-slot="table-body"
      className={cn("[&_tr:last-child]:border-0", className)}
      {...props}
    />
  );
}

function TableFooter({ className, ...props }: React.ComponentProps<"tfoot">) {
  return (
    <tfoot
      data-slot="table-footer"
      className={cn(
        "border-t-2 border-border dark:border-white bg-white dark:bg-black font-base text-main-foreground dark:text-white last:[&>tr]:border-b-0",
        className
      )}
      {...props}
    />
  );
}

function TableRow({ className, ...props }: React.ComponentProps<"tr">) {
  return (
    <tr
      data-slot="table-row"
      className={cn(
        "border-b-2 border-border dark:border-white transition-colors text-main-foreground dark:text-white bg-white dark:bg-black font-base data-[state=selected]:bg-secondary-background dark:data-[state=selected]:bg-gray-900 data-[state=selected]:text-main-foreground dark:data-[state=selected]:text-white",
        className
      )}
      {...props}
    />
  );
}

